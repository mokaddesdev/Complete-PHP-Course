<?php
/**
 * PHP Statements
 */

/**
 * Output Statements
 * 1. echo vs print       => text output
 * 2. printf vs sprintf   => formatted output
 * 3. print_r vs var_dump => variable modify for checking variable condition
 */

/**
 * Expression Statement
 * $x = 10;
 */

/**
 * Conditional Statements
 * if       => decision making by condition
 * else     => decision making by condition
 * elseif   => decision making by condition
 * switch   => decision making by multiple (more than three) conditions
 */

/**
 * Looping Statements
 * for      => fixed number looping
 * while    => looping depends on condition fulfillment
 * do-while => condition checked after first execution
 */

/**
 * Flow Control Statements
 * break    => exit from loop or switch
 * continue => continue to next condition check
 * goto     => jump from one step to another step
 */

/**
 * Function Statement
 * function => declare a new function
 * return   => return value from function
 */

/**
 * Exception Handling Statements
 * try      => try to execute code block
 * catch    => catch/store error
 * finally  => executes after try block with or without error
 */

/**
 * Class and Object Statements
 * class     => declare a class
 * new       => create new object
 * public    => access modifier for public access
 * private   => access modifier for private access
 * protected => access modifier for protected access
 */